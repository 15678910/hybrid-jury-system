// Firestore 업로드 함수
// ============================================================
async function uploadPredictions() {
    const names = Object.keys(CLAUDE_PREDICTIONS);
    let success = 0;
    let fail = 0;
    let integrityMerged = 0;

    for (const name of names) {
        try {
            // 기존 예측 데이터에 사법정의 평가 + AI판사 비교 데이터 병합
            const prediction = { ...CLAUDE_PREDICTIONS[name] };
            if (JUDICIAL_INTEGRITY_DATA[name]) {
                const integrity = JUDICIAL_INTEGRITY_DATA[name];
                if (integrity.judicialIntegrity) prediction.judicialIntegrity = integrity.judicialIntegrity;
                if (integrity.aiJudgeComparison) prediction.aiJudgeComparison = integrity.aiJudgeComparison;
                integrityMerged++;
            }

            await db.collection('sentencingData').doc(name).set({
                claudePrediction: prediction,
                claudePredictionUpdatedAt: admin.firestore.FieldValue.serverTimestamp(),
                claudePredictionModel: 'claude-opus-4-6',
                claudePredictionVersion: '2026-02-23'
            }, { merge: true });
            success++;
            const tag = JUDICIAL_INTEGRITY_DATA[name] ? ' [+사법정의]' : '';
            process.stdout.write(`[${success + fail}/${names.length}] ${name}${tag} ... OK\n`);
        } catch (err) {
            fail++;
            process.stderr.write(`[${success + fail}/${names.length}] ${name} ... FAIL: ${err.message}\n`);
        }
    }

    process.stdout.write(`\n완료: 성공 ${success}건, 실패 ${fail}건 (총 ${names.length}명)\n`);
    process.stdout.write(`사법정의 데이터 병합: ${integrityMerged}명\n`);
    process.exit(fail > 0 ? 1 : 0);
}

uploadPredictions();
