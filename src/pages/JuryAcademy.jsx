    useEffect(() => {
        try {
            const saved = localStorage.getItem('juryAcademy_progress');
            if (saved) {
                const data = JSON.parse(saved);
                setCompletedModules(data.completedModules || []);
                setQuizAnswers(data.quizAnswers || {});
                setQuizSubmitted(data.quizSubmitted || {});
            }
        } catch (_) { /* 무시 */ }
    }, []);

    // 진행도 저장
